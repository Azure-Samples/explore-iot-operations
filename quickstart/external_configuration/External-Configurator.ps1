function Test-Prerequisites {
    Write-Log "Checking prerequisites..."
    
    $allPassed = $true
    
    # Check PowerShell version
    $psVersion = $PSVersionTable.PSVersion
    if ($psVersion.Major -lt 7) {
        Write-WarnLog "PowerShell 7+ is recommended. Current version: $psVersion"
        Write-WarnLog "Some features may not work correctly. Download PS7: https://aka.ms/install-powershell"
        Write-WarnLog "Continuing, but errors may occur."
    } else {
        Write-Success "PowerShell version: $psVersion"
    }
    
    # Check if Azure CLI is installed
    try {
        $azVersion = az version --output json 2>$null | ConvertFrom-Json
        $azCliVersion = $azVersion.'azure-cli'
        Write-Success "Azure CLI version: $azCliVersion"

        # Check minimum version 2.64.0
        $minVersion = [Version]"2.64.0"
        $currentAzVersion = [Version]$azCliVersion
        if ($currentAzVersion -lt $minVersion) {
            Write-WarnLog "Azure CLI $azCliVersion is below the recommended minimum 2.64.0."
            Write-WarnLog "This may cause deployment failures. Upgrade with: az upgrade"
            Write-WarnLog "Or reinstall from: https://aka.ms/installazurecliwindows"
            Write-WarnLog "Continuing, but errors may occur."
        }
    } catch {
        Write-ErrorLog "Azure CLI is not installed or not in PATH" -Fatal
    }
    
    # Check for iot-ops extension
    $extensions = az extension list --output json 2>$null | ConvertFrom-Json
    $iotOpsExt = $extensions | Where-Object { $_.name -eq 'azure-iot-ops' }
    
    if (-not $iotOpsExt) {
        Write-WarnLog "azure-iot-ops extension not found. Install it manually before continuing:"
        Write-WarnLog "  az extension add --name azure-iot-ops --upgrade"
        Write-WarnLog "Then re-run this script."
        $allPassed = $false
    } else {
        Write-Success "azure-iot-ops extension version: $($iotOpsExt.version)"
    }
    
    # Check ARM templates directory
    if (-not (Test-Path $script:ArmTemplatesDir)) {
        Write-ErrorLog "ARM templates directory not found: $script:ArmTemplatesDir" -Fatal
    }
    Write-Success "ARM templates directory found"
    
    # Check config directory
    if (-not (Test-Path $script:ConfigDir)) {
        Write-ErrorLog "Config directory not found: $script:ConfigDir" -Fatal
    }
    Write-Success "Config directory found"
    
    if (-not $allPassed) {
        Write-ErrorLog "Prerequisite checks failed" -Fatal
    }
    
    Write-Success "All prerequisites passed"
}

