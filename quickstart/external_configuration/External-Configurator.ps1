function Deploy-Infrastructure {
    Write-Log "Deploying Azure infrastructure via ARM templates..."
    
    # Step 1: Create Resource Group (check if exists first to minimize permission requirements)
    Write-Log "Step 1/7: Checking resource group..."
    
    $rgExists = az group exists --name $script:ResourceGroup 2>$null
    if ($rgExists -eq "true") {
        Write-Success "Resource group already exists: $script:ResourceGroup"
    } else {
        Write-Log "Resource group does not exist, creating via Azure CLI..."
        
        if ($DryRun) {
            Write-InfoLog "[DRY-RUN] Would create resource group: $script:ResourceGroup in $script:Location"
        } else {
            # Use az group create instead of ARM template - requires fewer permissions
            $rgResult = az group create `
                --name $script:ResourceGroup `
                --location $script:Location `
                --tags project=azure-iot-operations cluster=$script:ClusterName `
                --output json 2>&1
            
            if ($LASTEXITCODE -eq 0) {
                Write-Success "Resource group created: $script:ResourceGroup"
                $script:DeployedResources += "ResourceGroup:$script:ResourceGroup"
            } else {
                Write-ErrorLog "Failed to create resource group"
                Write-ErrorLog "Error: $rgResult"
                Write-Host ""
                Write-Host "You may need 'Contributor' role at the subscription level to create resource groups." -ForegroundColor Yellow
                Write-Host "Alternatively, ask your Azure admin to create the resource group for you:" -ForegroundColor Gray
                Write-Host "  az group create --name $script:ResourceGroup --location $script:Location" -ForegroundColor Cyan
                Write-Host ""
                Write-ErrorLog "Cannot continue without resource group" -Fatal
            }
        }
    }
    
    # Step 2: Create Storage Account
    Write-Log "Step 2/7: Creating storage account..."
    # Check if storage account already exists (name is globally unique across Azure)
    # First check in our resource group, then check global availability
    $existingStorage = az storage account show --name $script:StorageAccountName --resource-group $script:ResourceGroup --query "name" -o tsv 2>$null
    if ($existingStorage) {
        Write-Success "Storage account already exists in resource group: $script:StorageAccountName (reusing)"
        $storageResult = @{ success = $true }
    } else {
        # Check if the name is taken globally (by another RG/subscription)
        $nameCheck = az storage account check-name --name $script:StorageAccountName --query "nameAvailable" -o tsv 2>$null
        if ($nameCheck -eq "false") {
            # Name taken globally - check if it exists anywhere in our subscription
            $existingInSub = az storage account list --query "[?name=='$($script:StorageAccountName)'].{name:name, rg:resourceGroup}" -o json 2>$null | ConvertFrom-Json
            if ($existingInSub -and $existingInSub.Count -gt 0) {
                Write-WarnLog "Storage account '$($script:StorageAccountName)' exists in resource group '$($existingInSub[0].rg)' (different from '$($script:ResourceGroup)')"
                Write-Success "Reusing existing storage account: $script:StorageAccountName"
                $storageResult = @{ success = $true }
            } else {
                Write-WarnLog "Storage account name '$($script:StorageAccountName)' is taken globally by another Azure subscription."
                Write-Host "Please set a unique 'storage_account_name' in config/aio_config.json and retry." -ForegroundColor Yellow
                Write-ErrorLog "Cannot create storage account - name globally taken" -Fatal
            }
        } else {
            $storageResult = Deploy-ARMTemplate -TemplateName "storageAccount" -Parameters @{
                storageAccountName = $script:StorageAccountName
                location = $script:Location
            }
        }
    }
    
    # TODO (fabric-entra-id-gap): Key Vault + secret sync are required today specifically for
    # Fabric SASL auth. When Fabric supports Entra ID on custom Kafka endpoints, this step and
    # the secretsync enable below can be removed for the Fabric RTI flow. See issues/fabric_entra_id_gap.md.
    # Step 3: Create Key Vault
    Write-Log "Step 3/7: Creating Key Vault..."
    # Check if Key Vault already exists (name is globally unique across Azure)
    $existingKv = az keyvault show --name $script:KeyVaultName --resource-group $script:ResourceGroup --query "name" -o tsv 2>$null
    if ($existingKv) {
        Write-Success "Key Vault already exists in resource group: $script:KeyVaultName (reusing)"
        $kvResult = @{ success = $true }
    } else {
        # Check if the name is taken globally (including soft-deleted vaults)
        $existingKvAnywhere = az keyvault show --name $script:KeyVaultName --query "name" -o tsv 2>$null
        if ($existingKvAnywhere) {
            Write-WarnLog "Key Vault '$($script:KeyVaultName)' exists in a different resource group"
            Write-Success "Reusing existing Key Vault: $script:KeyVaultName"
            $kvResult = @{ success = $true }
        } else {
            $kvParams = @{
                keyVaultName = $script:KeyVaultName
                location = $script:Location
            }
            # Add user access policy if we have the user's object ID
            if ($script:UserObjectId) {
                $kvParams.userObjectId = $script:UserObjectId
                Write-InfoLog "Adding access policy for current user to Key Vault"
            }
            $kvResult = Deploy-ARMTemplate -TemplateName "keyVault" -Parameters $kvParams
        }
    }
    
    # Step 4: Create Device Registry Namespace
    Write-Log "Step 4/7: Creating Device Registry namespace..."
    $nsResult = Deploy-ARMTemplate -TemplateName "deviceRegistryNamespace" -Parameters @{
        namespaceName = $script:NamespaceName
        location = $script:Location
    }
    
    # Step 5: Create Schema Registry
    Write-Log "Step 5/7: Creating Schema Registry..."
    $srResult = Deploy-ARMTemplate -TemplateName "schemaRegistry" -Parameters @{
        schemaRegistryName = $script:SchemaRegistryName
        location = $script:Location
        storageAccountName = $script:StorageAccountName
    }
    
    # Step 6: Create Managed Identity for secret sync
    Write-Log "Step 6/7: Creating Managed Identity..."
    $miName = "$($script:ClusterName)-secretsync-mi"
    $miResult = Deploy-ARMTemplate -TemplateName "managedIdentity" -Parameters @{
        managedIdentityName = $miName
        location = $script:Location
    }
    
    # Configure Key Vault RBAC role assignments for managed identities
    # Using RBAC instead of access policies for better security and management
    Write-Log "Configuring Key Vault RBAC role assignments for AIO dataflows..."
    
    # Key Vault Secrets User role ID (built-in role for reading secrets)
    $kvSecretsUserRoleId = "4633458b-17de-408a-b874-0445c86b69e6"
    $kvResourceId = "/subscriptions/$script:SubscriptionId/resourceGroups/$script:ResourceGroup/providers/Microsoft.KeyVault/vaults/$script:KeyVaultName"
    
    if (-not $DryRun) {
        # Get the managed identity's principal ID
        $miPrincipalId = az identity show --name $miName --resource-group $script:ResourceGroup --query "principalId" -o tsv 2>$null
        
        if ($miPrincipalId) {
            Write-InfoLog "Assigning Key Vault Secrets User role to $miName..."
            az role assignment create `
                --role $kvSecretsUserRoleId `
                --assignee-object-id $miPrincipalId `
                --assignee-principal-type ServicePrincipal `
                --scope $kvResourceId `
                --output none 2>$null
            
            if ($LASTEXITCODE -eq 0) {
                Write-Success "Granted Key Vault Secrets User role to $miName"
            } else {
                Write-WarnLog "Failed to assign Key Vault role for $miName (may already exist)"
            }
        } else {
            Write-WarnLog "Could not get principal ID for $miName"
        }
        
        # Also check for any other managed identities in the resource group and grant them access
        $allManagedIdentities = az identity list --resource-group $script:ResourceGroup --query "[].{name:name, principalId:principalId}" 2>$null | ConvertFrom-Json
        
        if ($allManagedIdentities) {
            foreach ($identity in $allManagedIdentities) {
                # Skip the one we just configured
                if ($identity.name -eq $miName) { continue }
                
                # Only configure identities that look like they're for secret sync or AIO
                if ($identity.name -match "secretsync|aio|iot") {
                    Write-InfoLog "Assigning Key Vault Secrets User role to $($identity.name)..."
                    az role assignment create `
                        --role $kvSecretsUserRoleId `
                        --assignee-object-id $identity.principalId `
                        --assignee-principal-type ServicePrincipal `
                        --scope $kvResourceId `
                        --output none 2>$null
                    
                    if ($LASTEXITCODE -eq 0) {
                        Write-Success "Granted Key Vault Secrets User role to $($identity.name)"
                    }
                }
            }
        }
    } else {
        Write-InfoLog "[DRY RUN] Would configure Key Vault RBAC role assignments for managed identities"
    }
    
    # Step 7: Create Container Registry
    Write-Log "Step 7/7: Creating Container Registry..."
    $existingAcr = az acr show --name $script:ContainerRegistryName --resource-group $script:ResourceGroup --query "name" -o tsv 2>$null
    if ($existingAcr) {
        Write-Success "Container registry already exists: $script:ContainerRegistryName (reusing)"
        $script:ContainerRegistryLoginServer = az acr show --name $script:ContainerRegistryName --query "loginServer" -o tsv 2>$null
    } else {
        # Check global name availability (ACR names are globally unique)
        $acrNameCheck = az acr check-name --name $script:ContainerRegistryName --query "nameAvailable" -o tsv 2>$null
        if ($acrNameCheck -eq "false") {
            Write-WarnLog "ACR name '$($script:ContainerRegistryName)' is already taken globally."
            Write-WarnLog "Set a unique 'container_registry' name in config/aio_config.json and retry."
        } else {
            $acrResult = Deploy-ARMTemplate -TemplateName "containerRegistry" -Parameters @{
                registryName = $script:ContainerRegistryName
                location     = $script:Location
            }
            if ($acrResult.success) {
                $script:ContainerRegistryLoginServer = az acr show --name $script:ContainerRegistryName --query "loginServer" -o tsv 2>$null
                Write-Success "Container registry created: $script:ContainerRegistryLoginServer"
                $script:DeployedResources += "ContainerRegistry:$script:ContainerRegistryName"

                # Update aio_config.json with the login server so Deploy-EdgeModules.ps1 picks it up
                $configPath = Join-Path $script:ConfigDir "aio_config.json"
                if (Test-Path $configPath) {
                    try {
                        $cfg = Get-Content $configPath -Raw | ConvertFrom-Json
                        $cfg.azure.container_registry = $script:ContainerRegistryLoginServer
                        $cfg | ConvertTo-Json -Depth 10 | Set-Content $configPath -Encoding UTF8
                        Write-InfoLog "Updated aio_config.json: container_registry = $script:ContainerRegistryLoginServer"
                    } catch {
                        Write-WarnLog "Could not auto-update aio_config.json with registry login server: $_"
                    }
                }
            }
        }
    }

    # Role assignments are handled by grant_entra_id_roles.ps1
    # This separation allows different people (admin vs developer) to run different scripts
    Write-Host ""
    Write-Host "============================================================================" -ForegroundColor Cyan
    Write-Host "ROLE ASSIGNMENTS REMINDER" -ForegroundColor Cyan
    Write-Host "============================================================================" -ForegroundColor Cyan
    Write-Host ""
    Write-Host "Infrastructure resources have been created. Role assignments (RBAC) are" -ForegroundColor Yellow
    Write-Host "handled separately by grant_entra_id_roles.ps1 to allow separation of duties." -ForegroundColor Yellow
    Write-Host ""
    Write-Host "If this is your first run, or if new resources were created, run:" -ForegroundColor Cyan
    Write-Host ""
    Write-Host "  .\grant_entra_id_roles.ps1" -ForegroundColor Green
    Write-Host ""
    Write-Host "This grants permissions like:" -ForegroundColor Gray
    Write-Host "  - Schema Registry -> Storage Blob Data Contributor" -ForegroundColor Gray
    Write-Host "  - Managed Identity -> Key Vault Secrets User" -ForegroundColor Gray
    Write-Host "  - AIO Instance -> Event Hubs permissions for Fabric" -ForegroundColor Gray
    Write-Host "  - AIO Instance -> AcrPull on Container Registry (via grant_entra_id_roles.ps1)" -ForegroundColor Gray
    Write-Host ""
    Write-Host "============================================================================" -ForegroundColor Cyan
    Write-Host ""
    
    Write-Success "Infrastructure deployment completed"
}

