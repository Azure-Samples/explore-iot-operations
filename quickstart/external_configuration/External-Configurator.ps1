# This script assumes the cluster is already Arc-enabled

function Register-AcrRegistryEndpoint {
    param(
        [Parameter(Mandatory=$true)]
        [string]$InstanceName
    )

    if (-not $script:ContainerRegistryLoginServer) {
        Write-WarnLog "No container registry configured - skipping registry endpoint registration."
        return
    }

    Write-Log "Registering ACR as AIO registry endpoint: $script:ContainerRegistryLoginServer"

    if ($DryRun) {
        Write-InfoLog "[DRY-RUN] Would register registry endpoint for $script:ContainerRegistryLoginServer"
        return
    }

    # Derive a consistent endpoint name from the registry name
    $endpointName = "$($script:ContainerRegistryName)-endpoint"

    # --- Step 1: Ensure AcrPull is granted to the AIO managed identity (safety net) ---
    # grant_entra_id_roles.ps1 handles this in the normal flow; this is a fallback
    # for cases where it hasn't been run yet or ran before AIO was deployed.
    Write-InfoLog "Ensuring AcrPull role is assigned to AIO instance managed identity..."
    $aioIdentity = az iot ops show `
        --name $InstanceName `
        --resource-group $script:ResourceGroup `
        --query "identity.principalId" -o tsv 2>$null

    $acrResourceId = az acr show `
        --name $script:ContainerRegistryName `
        --resource-group $script:ResourceGroup `
        --query "id" -o tsv 2>$null

    if ($aioIdentity -and $acrResourceId) {
        az role assignment create `
            --role "AcrPull" `
            --assignee-object-id $aioIdentity `
            --assignee-principal-type ServicePrincipal `
            --scope $acrResourceId `
            --output none 2>$null
        # Non-zero exit is expected when the assignment already exists - not fatal
        Write-InfoLog "AcrPull role assignment applied (idempotent)."
    } else {
        Write-WarnLog "Could not resolve AIO identity or ACR resource ID - AcrPull may need to be set manually via grant_entra_id_roles.ps1."
    }

    # --- Step 2: Check if the registry endpoint is already registered ---
    $existingEndpoint = az iot ops registry list `
        --instance $InstanceName `
        --resource-group $script:ResourceGroup `
        --query "[?properties.server=='$script:ContainerRegistryLoginServer'].name" `
        -o tsv 2>$null

    if ($existingEndpoint) {
        Write-Success "Registry endpoint already registered for $script:ContainerRegistryLoginServer ($existingEndpoint) - skipping."
        return
    }

    # --- Step 3: Create the registry endpoint ---
    Write-Log "Creating registry endpoint '$endpointName'..."
    az iot ops registry create `
        --name $endpointName `
        --host $script:ContainerRegistryLoginServer `
        --instance $InstanceName `
        --resource-group $script:ResourceGroup `
        --output none 2>&1

    if ($LASTEXITCODE -eq 0) {
        Write-Success "Registry endpoint created: $endpointName -> $script:ContainerRegistryLoginServer"
        $script:DeployedResources += "RegistryEndpoint:$endpointName"
    } else {
        Write-WarnLog "Registry endpoint creation returned non-zero. It may already exist or the AIO instance is not yet ready."
        Write-WarnLog "You can register it manually with:"
        Write-WarnLog "  az iot ops registry create -n $endpointName --host $script:ContainerRegistryLoginServer -i $InstanceName -g $script:ResourceGroup"
    }
}

function Deploy-IoTOperations {
    if ($SkipIoTOps) {
        Write-InfoLog "Skipping IoT Operations deployment (-SkipIoTOps flag)"
        return
    }
    
    Write-Log "Deploying Azure IoT Operations..."
    Write-InfoLog "Note: IoT Operations deployment requires Azure CLI (no ARM template available)"
    
    # Enable UTF-8 encoding
    [Console]::OutputEncoding = [System.Text.Encoding]::UTF8
    $env:PYTHONIOENCODING = "utf-8"
    $env:PYTHONUTF8 = "1"
    chcp 65001 2>&1 | Out-Null
    
    $instanceName = "$script:ClusterName-aio"
    
    if ($DryRun) {
        Write-InfoLog "[DRY-RUN] Would deploy Azure IoT Operations instance: $instanceName"
        Write-InfoLog "[DRY-RUN] az iot ops init --cluster $script:ClusterName --resource-group $script:ResourceGroup"
        Write-InfoLog "[DRY-RUN] az iot ops create --cluster $script:ClusterName --resource-group $script:ResourceGroup --name $instanceName"
        return
    }
    
    # Check if already deployed
    $existingInstance = az iot ops show --name $instanceName --resource-group $script:ResourceGroup 2>&1
    
    $aioAlreadyExists = $LASTEXITCODE -eq 0
    
    if ($aioAlreadyExists) {
        Write-Success "Azure IoT Operations instance $instanceName already exists"
        Write-InfoLog "Verifying Key Vault access policies are configured..."
    } else {
        # Check Arc cluster connectivity
        Write-Log "Checking Arc cluster connectivity..."
        $clusterResult = az connectedk8s show `
        --name $script:ClusterName `
        --resource-group $script:ResourceGroup `
        --output json 2>&1
    
    if ($LASTEXITCODE -ne 0) {
        Write-Host ""
        Write-Host "============================================================================" -ForegroundColor Red
        Write-Host "ARC CLUSTER NOT FOUND" -ForegroundColor Red
        Write-Host "============================================================================" -ForegroundColor Red
        Write-Host ""
        Write-Host "The Arc-enabled cluster '$script:ClusterName' was not found in resource group '$script:ResourceGroup'." -ForegroundColor Yellow
        Write-Host ""
        Write-Host "This means Arc enablement has not been completed yet." -ForegroundColor Yellow
        Write-Host ""
        Write-Host "To fix this:" -ForegroundColor Cyan
        Write-Host ""
        Write-Host "  1. On your EDGE DEVICE (Linux), run:" -ForegroundColor Green
        Write-Host "     pwsh ./arc_enable.ps1" -ForegroundColor White
        Write-Host ""
        Write-Host "  2. Wait for it to complete, then run this script again." -ForegroundColor Green
        Write-Host ""
        Write-Host "If you already ran arc_enable.ps1, check:" -ForegroundColor Cyan
        Write-Host "  - Did it complete successfully?" -ForegroundColor Gray
        Write-Host "  - Is the cluster name correct? Expected: $script:ClusterName" -ForegroundColor Gray
        Write-Host "  - Is the resource group correct? Expected: $script:ResourceGroup" -ForegroundColor Gray
        Write-Host ""
        Write-Host "To verify Arc status from the edge device:" -ForegroundColor Cyan
        Write-Host "  kubectl get pods -n azure-arc" -ForegroundColor White
        Write-Host ""
        Write-Host "============================================================================" -ForegroundColor Red
        Write-ErrorLog "Arc cluster not found - cannot deploy IoT Operations" -Fatal
    }
    
    $clusterJson = if ($clusterResult -is [array]) { $clusterResult -join "`n" } else { $clusterResult }
    $clusterData = $clusterJson | ConvertFrom-Json
    $clusterStatus = $clusterData.connectivityStatus
    
    if ($clusterStatus -ne "Connected") {
        Write-Host ""
        Write-Host "============================================================================" -ForegroundColor Red
        Write-Host "ARC CLUSTER NOT CONNECTED" -ForegroundColor Red
        Write-Host "============================================================================" -ForegroundColor Red
        Write-Host ""
        Write-Host "The Arc cluster exists but is not connected." -ForegroundColor Yellow
        Write-Host "  Cluster: $script:ClusterName" -ForegroundColor Gray
        Write-Host "  Status:  $clusterStatus" -ForegroundColor Gray
        Write-Host ""
        Write-Host "The cluster must be 'Connected' before IoT Operations can be deployed." -ForegroundColor Yellow
        Write-Host ""
        Write-Host "Troubleshooting steps (run on the EDGE DEVICE):" -ForegroundColor Cyan
        Write-Host ""
        Write-Host "  1. Check if K3s is running:" -ForegroundColor Green
        Write-Host "     sudo systemctl status k3s" -ForegroundColor White
        Write-Host ""
        Write-Host "  2. Check Arc agent pods:" -ForegroundColor Green
        Write-Host "     kubectl get pods -n azure-arc" -ForegroundColor White
        Write-Host ""
        Write-Host "  3. Check Arc agent logs:" -ForegroundColor Green
        Write-Host "     kubectl logs -n azure-arc -l app.kubernetes.io/component=connect-agent --tail=50" -ForegroundColor White
        Write-Host ""
        Write-Host "  4. If pods are not running, restart K3s:" -ForegroundColor Green
        Write-Host "     sudo systemctl restart k3s" -ForegroundColor White
        Write-Host ""
        Write-Host "  5. Check network connectivity from edge device:" -ForegroundColor Green
        Write-Host "     curl -s https://management.azure.com" -ForegroundColor White
        Write-Host ""
        Write-Host "Common issues:" -ForegroundColor Cyan
        Write-Host "  - Network/firewall blocking outbound connections to Azure" -ForegroundColor Gray
        Write-Host "  - DNS resolution issues on the edge device" -ForegroundColor Gray
        Write-Host "  - K3s service not running" -ForegroundColor Gray
        Write-Host "  - Arc agents crashed (check pod status)" -ForegroundColor Gray
        Write-Host ""
        Write-Host "After fixing the issue, check status with:" -ForegroundColor Cyan
        Write-Host "  az connectedk8s show --name $script:ClusterName --resource-group $script:ResourceGroup --query connectivityStatus" -ForegroundColor White
        Write-Host ""
        Write-Host "============================================================================" -ForegroundColor Red
        Write-ErrorLog "Arc cluster not connected (status: $clusterStatus) - cannot deploy IoT Operations" -Fatal
    }
    
    Write-Success "Arc cluster status: Connected"
    
    # Check if custom-locations feature is enabled (required for IoT Operations)
    Write-Log "Checking custom-locations feature on Arc cluster..."
    
    # Get the Custom Locations Resource Provider object ID for this tenant
    # The Application ID bc313c14-388c-4e7d-a58e-70017303ee3b is fixed globally for the Custom Locations RP
    $customLocationsAppId = "bc313c14-388c-4e7d-a58e-70017303ee3b"
    $customLocationsOid = az ad sp show --id $customLocationsAppId --query id -o tsv 2>$null
    
    if ([string]::IsNullOrEmpty($customLocationsOid)) {
        Write-WarnLog "Could not retrieve Custom Locations Resource Provider object ID"
    } else {
        Write-InfoLog "Custom Locations RP Object ID: $customLocationsOid"
    }
    
    # Check cluster properties - custom-locations is considered enabled if:
    # 1. Cluster is Connected
    # 2. PrivateLinkState is Disabled (custom-locations incompatible with Private Link)
    # Note: The 'features' property in az connectedk8s show is NOT populated when
    # custom-locations is enabled via -CustomLocationsOid during New-AzConnectedKubernetes.
    # The feature is still enabled, it just doesn't show in that property.
    $clusterInfoRaw = az connectedk8s show `
        --name $script:ClusterName `
        --resource-group $script:ResourceGroup `
        -o json 2>$null
    $clusterInfoJson = if ($clusterInfoRaw -is [array]) { $clusterInfoRaw -join "`n" } else { $clusterInfoRaw }
    $clusterInfo = $clusterInfoJson | ConvertFrom-Json
    
    $customLocationsEnabled = $false
    $blockingReason = ""
    
    if ($clusterInfo) {
        # Check for blocking conditions
        if ($clusterInfo.privateLinkState -eq "Enabled") {
            $blockingReason = "Private Link is enabled (incompatible with custom-locations)"
        } elseif ($clusterInfo.connectivityStatus -ne "Connected") {
            $blockingReason = "Cluster is not connected (status: $($clusterInfo.connectivityStatus))"
        } else {
            # Cluster is connected with Private Link disabled - custom-locations should work
            # The feature was enabled via -CustomLocationsOid during arc_enable.ps1
            $customLocationsEnabled = $true
            
            # Also check the features array if it exists (belt and suspenders)
            if ($clusterInfo.features) {
                foreach ($feature in $clusterInfo.features) {
                    if ($feature.name -eq "custom-locations" -and $feature.state -eq "Installed") {
                        Write-InfoLog "custom-locations feature explicitly confirmed in cluster properties"
                        break
                    }
                }
            }
        }
    } else {
        $blockingReason = "Could not retrieve cluster information"
    }
    
    if ($customLocationsEnabled) {
        Write-Success "Custom-locations feature is enabled (cluster connected, Private Link disabled)"
        Write-WarnLog "NOTE: ARM shows custom-locations is configured, but the Helm chart must also be updated."
        Write-InfoLog "If 'az iot ops create' fails with 'resource provider does not have required permissions',"
        Write-InfoLog "you need to run this on your EDGE device:"
        Write-Host ""
        Write-Host "  az connectedk8s enable-features \" -ForegroundColor Cyan
        Write-Host "    --name $script:ClusterName \" -ForegroundColor Cyan
        Write-Host "    --resource-group $script:ResourceGroup \" -ForegroundColor Cyan
        Write-Host "    --features cluster-connect custom-locations \" -ForegroundColor Cyan
        Write-Host "    --custom-locations-oid $customLocationsOid" -ForegroundColor Cyan
        Write-Host ""
        Write-InfoLog "Or re-run arc_enable.ps1 on the edge device (it will do this for you)."
        Write-Host ""
    } else {
        Write-Host ""
        Write-Host "============================================================================" -ForegroundColor Red
        Write-Host "CUSTOM-LOCATIONS CANNOT BE VERIFIED" -ForegroundColor Red
        Write-Host "============================================================================" -ForegroundColor Red
        Write-Host ""
        Write-Host "Reason: $blockingReason" -ForegroundColor Yellow
        Write-Host ""
        Write-Host "The custom-locations feature is required for Azure IoT Operations." -ForegroundColor Yellow
        Write-Host ""
        Write-Host "If you ran arc_enable.ps1 on the edge device and it completed successfully," -ForegroundColor Cyan
        Write-Host "you also need to run the helm upgrade step on the edge device." -ForegroundColor Cyan
        Write-Host ""
        Write-Host "IMPORTANT: The Az.ConnectedKubernetes PowerShell module has a gap where" -ForegroundColor Yellow
        Write-Host "-CustomLocationsOid only registers with ARM but doesn't update Helm." -ForegroundColor Yellow
        Write-Host ""
        Write-Host "To fix, on your EDGE DEVICE:" -ForegroundColor Cyan
        Write-Host ""
        Write-Host "  # Check if custom-locations is enabled in Helm:" -ForegroundColor Gray
        Write-Host "  helm get values azure-arc --namespace azure-arc-release -o json | jq '.systemDefaultValues.customLocations'" -ForegroundColor White
        Write-Host ""
        Write-Host "  # If it shows 'null', run:" -ForegroundColor Gray
        Write-Host "  az connectedk8s enable-features \" -ForegroundColor White
        Write-Host "    --name $script:ClusterName \" -ForegroundColor White
        Write-Host "    --resource-group $script:ResourceGroup \" -ForegroundColor White
        Write-Host "    --features cluster-connect custom-locations \" -ForegroundColor White
        Write-Host "    --custom-locations-oid $customLocationsOid" -ForegroundColor White
        Write-Host ""
        Write-Host "  # Or re-run arc_enable.ps1 (includes this step):" -ForegroundColor Gray
        Write-Host "  cd ~/learn-iot/arc_build_linux && git pull" -ForegroundColor White
        Write-Host "  pwsh ./arc_enable.ps1" -ForegroundColor White
        Write-Host ""
        Write-Host "Then re-run this script." -ForegroundColor Cyan
        Write-Host ""
        Write-Host "============================================================================" -ForegroundColor Red
        Write-ErrorLog "Custom-locations feature not enabled - cannot deploy IoT Operations. Reason: $blockingReason" -Fatal
    }
    
    # Check if IoT Operations already exists on this cluster
    $existingInstance = az iot ops show --name $instanceName --resource-group $script:ResourceGroup --query "provisioningState" -o tsv 2>$null
    if ($existingInstance) {
        Write-Success "IoT Operations instance already exists: $instanceName (State: $existingInstance) - skipping create"
        
        # Try upgrade instead to pick up any config changes
        Write-Log "Running IoT Operations upgrade to apply any pending updates..."
        az iot ops upgrade --name $instanceName --resource-group $script:ResourceGroup -y 2>&1
        if ($LASTEXITCODE -eq 0) {
            Write-Success "IoT Operations upgrade completed"
        } else {
            Write-WarnLog "IoT Operations upgrade returned non-zero (may already be up to date)"
        }
    } else {
        # Initialize cluster
        Write-Log "Initializing cluster for Azure IoT Operations..."
        az iot ops init --cluster $script:ClusterName --resource-group $script:ResourceGroup
        
        # Get schema registry ID
        $schemaRegistryId = az resource show `
            --resource-group $script:ResourceGroup `
            --resource-type "Microsoft.DeviceRegistry/schemaRegistries" `
            --name $script:SchemaRegistryName `
            --query "id" -o tsv 2>$null
        
        # Get namespace ID
        $namespaceId = "/subscriptions/$script:SubscriptionId/resourceGroups/$script:ResourceGroup/providers/Microsoft.DeviceRegistry/namespaces/$script:NamespaceName"
        
        Write-Log "Deploying Azure IoT Operations instance..."
        Write-InfoLog "This may take several minutes..."
        
        # Build az iot ops create argument list; append DemoMode broker flags if requested
        $createArgs = @(
            "iot", "ops", "create",
            "--cluster", $script:ClusterName,
            "--resource-group", $script:ResourceGroup,
            "--name", $instanceName,
            "--sr-resource-id", $schemaRegistryId,
            "--ns-resource-id", $namespaceId,
            "--no-progress"
        )
        if ($DemoMode) {
            Write-InfoLog "[DEMO] Applying low-resource broker sizing (~303 MiB RAM vs ~15.8 GB default)"
            Write-InfoLog "[DEMO] Ref: https://learn.microsoft.com/azure/iot-operations/manage-mqtt-broker/howto-configure-availability-scale"
            $createArgs += "--broker-frontend-replicas", "1",
                           "--broker-frontend-workers",  "1",
                           "--broker-backend-part",      "1",
                           "--broker-backend-rf",        "2",
                           "--broker-backend-workers",   "1",
                           "--broker-mem-profile",       "Tiny"
        }
        az @createArgs 2>&1
        
        if ($LASTEXITCODE -ne 0) {
            Write-ErrorLog "Azure IoT Operations deployment failed"
            return
        }
        
        Write-Success "Azure IoT Operations deployed: $instanceName"
    }
    $script:DeployedResources += "IoTOperationsInstance:$instanceName"

    # Enable secret sync
    Write-Log "Enabling secret sync..."
    $miName = "$($script:ClusterName)-secretsync-mi"
    $miResourceId = az identity show `
        --name $miName `
        --resource-group $script:ResourceGroup `
        --query "id" -o tsv 2>$null
    
    if ($miResourceId) {
        # IMPORTANT: Key Vault RBAC role must be assigned BEFORE enabling secretsync
        Write-InfoLog "Assigning Key Vault Secrets User role to secret sync managed identity (required before secretsync)..."
        $miPrincipalId = az identity show --name $miName --resource-group $script:ResourceGroup --query "principalId" -o tsv 2>$null
        
        # Key Vault Secrets User role ID
        $kvSecretsUserRoleId = "4633458b-17de-408a-b874-0445c86b69e6"
        $kvResourceId = "/subscriptions/$script:SubscriptionId/resourceGroups/$script:ResourceGroup/providers/Microsoft.KeyVault/vaults/$script:KeyVaultName"
        
        if ($miPrincipalId) {
            az role assignment create `
                --role $kvSecretsUserRoleId `
                --assignee-object-id $miPrincipalId `
                --assignee-principal-type ServicePrincipal `
                --scope $kvResourceId `
                --output none 2>$null
            
            if ($LASTEXITCODE -eq 0) {
                Write-Success "Granted Key Vault Secrets User role to $miName"
            } else {
                Write-WarnLog "Failed to assign Key Vault role for $miName - secretsync may fail (role may already exist)"
            }
        }
        
        # TODO (fabric-entra-id-gap): secretsync enable is required today for Fabric SASL auth.
        # See issues/fabric_entra_id_gap.md.
        # Now enable secretsync
        az iot ops secretsync enable `
            --name $instanceName `
            --resource-group $script:ResourceGroup `
            --mi-user-assigned $miResourceId `
            --kv-resource-id $kvResourceId 2>$null
        
        Write-Success "Secret sync enabled"
        }
    }  # End of else block (AIO deployment)

    # Register ACR as a named registry endpoint inside AIO
    # (runs whether AIO was just created or already existed)
    Register-AcrRegistryEndpoint -InstanceName $instanceName

    # Configure Key Vault RBAC role assignments for Arc cluster and AIO instance
    # This runs whether AIO was just deployed or already existed
    Write-Log "Configuring Key Vault RBAC role assignments for AIO identities..."
    
    # Key Vault Secrets User role ID
    $kvSecretsUserRoleId = "4633458b-17de-408a-b874-0445c86b69e6"
    $kvResourceId = "/subscriptions/$script:SubscriptionId/resourceGroups/$script:ResourceGroup/providers/Microsoft.KeyVault/vaults/$script:KeyVaultName"
    
    # Grant Arc cluster identity access to Key Vault
    $arcIdentity = az connectedk8s show --name $script:ClusterName --resource-group $script:ResourceGroup --query "identity.principalId" -o tsv 2>$null
    if ($arcIdentity) {
        Write-InfoLog "Assigning Key Vault Secrets User role to Arc cluster identity..."
        az role assignment create `
            --role $kvSecretsUserRoleId `
            --assignee-object-id $arcIdentity `
            --assignee-principal-type ServicePrincipal `
            --scope $kvResourceId `
            --output none 2>$null
        
        if ($LASTEXITCODE -eq 0) {
            Write-Success "Granted Key Vault Secrets User role to Arc cluster identity"
        } else {
            Write-WarnLog "Failed to assign Key Vault role for Arc cluster identity (may already exist)"
        }
    }
    
    # Grant AIO instance identity access to Key Vault
    $aioIdentity = az iot ops show --name $instanceName --resource-group $script:ResourceGroup --query "identity.principalId" -o tsv 2>$null
    if ($aioIdentity) {
        Write-InfoLog "Assigning Key Vault Secrets User role to AIO instance identity..."
        az role assignment create `
            --role $kvSecretsUserRoleId `
            --assignee-object-id $aioIdentity `
            --assignee-principal-type ServicePrincipal `
            --scope $kvResourceId `
            --output none 2>$null
        
        if ($LASTEXITCODE -eq 0) {
            Write-Success "Granted Key Vault Secrets User role to AIO instance identity"
        } else {
            Write-WarnLog "Failed to assign Key Vault role for AIO instance identity (may already exist)"
        }
    }
}

