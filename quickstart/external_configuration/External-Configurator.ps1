function Import-ClusterInfo {
    Write-Log "Loading cluster information..."
    
    # Determine cluster info path
    $clusterInfoPath = $null
    
    if ($ClusterInfo) {
        if ([System.IO.Path]::IsPathRooted($ClusterInfo)) {
            $clusterInfoPath = $ClusterInfo
        } else {
            $clusterInfoPath = Join-Path $script:ScriptDir $ClusterInfo
        }
    } else {
        # Default: look in config folder
        $clusterInfoPath = Join-Path $script:ConfigDir "cluster_info.json"
    }
    
    if (-not (Test-Path $clusterInfoPath)) {
        Write-WarnLog "Cluster info file not found: $clusterInfoPath"
        Write-Host ""
        Write-Host "cluster_info.json is optional." -ForegroundColor Yellow
        Write-Host "  - AKS-EE / single-machine users: continue without it (cluster name comes from config/env)." -ForegroundColor Gray
        Write-Host "  - K3s edge device users: copy config/cluster_info.json from your edge device to: $script:ConfigDir" -ForegroundColor Gray
        Write-Host ""
        $script:ClusterData = $null
        return
    }
    
    try {
        $script:ClusterData = Get-Content $clusterInfoPath -Raw | ConvertFrom-Json
        
        Write-Host ""
        Write-Host "Cluster Information:" -ForegroundColor Cyan
        Write-Host "  Cluster Name: $($script:ClusterData.cluster_name)" -ForegroundColor Gray
        Write-Host "  Node Name: $($script:ClusterData.node_name)" -ForegroundColor Gray
        Write-Host "  Node IP: $($script:ClusterData.node_ip)" -ForegroundColor Gray
        Write-Host "  Kubernetes Version: $($script:ClusterData.kubernetes_version)" -ForegroundColor Gray
        Write-Host "  Node OS: $($script:ClusterData.node_os)" -ForegroundColor Gray
        Write-Host "  Ready for Arc: $($script:ClusterData.ready_for_arc)" -ForegroundColor Gray
        Write-Host "  Installer Version: $($script:ClusterData.installer_version)" -ForegroundColor Gray
        Write-Host ""
        
        # Set cluster name from cluster info
        $script:ClusterName = $script:ClusterData.cluster_name
        
        Write-Success "Cluster information loaded from: $clusterInfoPath"
    } catch {
        Write-ErrorLog "Failed to parse cluster info file: $_" -Fatal
    }
}

function Import-AzureConfig {
    Write-Log "Loading Azure configuration..."
    
    # Determine config path
    $configPath = $null
    
    if ($ConfigFile) {
        if ([System.IO.Path]::IsPathRooted($ConfigFile)) {
            $configPath = $ConfigFile
        } else {
            $configPath = Join-Path $script:ScriptDir $ConfigFile
        }
    } else {
        # Default: look in config folder
        $configPath = Join-Path $script:ConfigDir "aio_config.json"
    }
    
    # Normalise env var aliases FIRST (before any early return) so they are available
    # regardless of whether a config file is found.
    # session-bootstrap.ps1 uses AZ_* / AKS_EDGE_* names; scripts check AZURE_* / AKSEDGE_*.
    if ([string]::IsNullOrEmpty($env:AZURE_SUBSCRIPTION_ID)    -and -not [string]::IsNullOrEmpty($env:AZ_SUBSCRIPTION_ID))    { $env:AZURE_SUBSCRIPTION_ID    = $env:AZ_SUBSCRIPTION_ID }
    if ([string]::IsNullOrEmpty($env:AZURE_RESOURCE_GROUP)     -and -not [string]::IsNullOrEmpty($env:AZ_RESOURCE_GROUP))     { $env:AZURE_RESOURCE_GROUP     = $env:AZ_RESOURCE_GROUP }
    if ([string]::IsNullOrEmpty($env:AZURE_LOCATION)           -and -not [string]::IsNullOrEmpty($env:AZ_LOCATION))           { $env:AZURE_LOCATION           = $env:AZ_LOCATION }
    if ([string]::IsNullOrEmpty($env:AKSEDGE_CLUSTER_NAME)     -and -not [string]::IsNullOrEmpty($env:AKS_EDGE_CLUSTER_NAME)) { $env:AKSEDGE_CLUSTER_NAME     = $env:AKS_EDGE_CLUSTER_NAME }
    if ([string]::IsNullOrEmpty($env:AZURE_CONTAINER_REGISTRY) -and -not [string]::IsNullOrEmpty($env:AZ_CONTAINER_REGISTRY)) { $env:AZURE_CONTAINER_REGISTRY = $env:AZ_CONTAINER_REGISTRY }

    Write-Host "[CONFIG] Looking for aio_config.json at: $configPath" -ForegroundColor Gray
    if (-not (Test-Path $configPath)) {
        Write-WarnLog "Config file not found: $configPath"
        Write-Host ""
        Write-Host "Configuration will be collected interactively." -ForegroundColor Yellow
        Write-Host "To use a config file, copy from template:" -ForegroundColor Yellow
        Write-Host "  cp config/quickstart_config.template config/aio_config.json" -ForegroundColor Gray
        Write-Host ""
    }
    
    try {
        $script:AzureConfig = Get-Content $configPath -Raw | ConvertFrom-Json
        
        # Load Azure settings
        if ($script:AzureConfig.azure) {
            $script:SubscriptionId = $script:AzureConfig.azure.subscription_id
            $script:ResourceGroup = $script:AzureConfig.azure.resource_group
            $script:Location = $script:AzureConfig.azure.location
            $rawNamespace = $script:AzureConfig.azure.namespace_name
            if ($rawNamespace) {
                $script:NamespaceName = $rawNamespace.ToLower()
                if ($script:NamespaceName -cne $rawNamespace) { Write-Warning "[CONFIG] namespace_name '$rawNamespace' was converted to lowercase: '$script:NamespaceName'" }
            }

            $rawStorage = $script:AzureConfig.azure.storage_account_name
            if ($rawStorage) {
                $script:StorageAccountName = $rawStorage.ToLower()
                if ($script:StorageAccountName -cne $rawStorage) { Write-Warning "[CONFIG] storage_account_name '$rawStorage' was converted to lowercase: '$script:StorageAccountName'" }
            }

            $rawKv = $script:AzureConfig.azure.key_vault_name
            if ($rawKv) {
                $script:KeyVaultName = $rawKv.ToLower()
                if ($script:KeyVaultName -cne $rawKv) { Write-Warning "[CONFIG] key_vault_name '$rawKv' was converted to lowercase: '$script:KeyVaultName'" }
            }
            
            # Store the config cluster name for validation (don't override yet)
            $script:ConfigClusterName = $script:AzureConfig.azure.cluster_name

            # Load container registry (either short name or login server)
            if ($script:AzureConfig.azure.PSObject.Properties['container_registry'] -and $script:AzureConfig.azure.container_registry) {
                $rawReg = $script:AzureConfig.azure.container_registry
                if ($rawReg -match '\.azurecr\.io$') {
                    $script:ContainerRegistryLoginServer = $rawReg
                    $script:ContainerRegistryName = $rawReg -replace '\.azurecr\.io$', ''
                } else {
                    $script:ContainerRegistryName = $rawReg
                    $script:ContainerRegistryLoginServer = "${rawReg}.azurecr.io"
                }
            }
        }
        
        Write-Success "Azure configuration loaded from: $configPath"
    } catch {
        Write-ErrorLog "Failed to parse config file: $_"
        Write-WarnLog "Configuration will be collected interactively"
    }

    # Step 2: Environment variables — only fill in values still empty after config file
    Write-Host "[CONFIG] Step 2: Environment variables (fallback for values missing from config)" -ForegroundColor Gray

    # Normalise aliases: session-bootstrap uses AZ_* / AKS_EDGE_* names; scripts use AZURE_* / AKSEDGE_*
    # Accept both so users don't have to know the difference.
    if (-not [string]::IsNullOrEmpty($env:AZURE_SUBSCRIPTION_ID)) {
        if ([string]::IsNullOrEmpty($script:SubscriptionId)) {
            $script:SubscriptionId = $env:AZURE_SUBSCRIPTION_ID
            Write-InfoLog "  [env] AZURE_SUBSCRIPTION_ID    = $script:SubscriptionId"
        } else { Write-InfoLog "  [env] AZURE_SUBSCRIPTION_ID    = (skipped - config file value used)" }
    } else { Write-InfoLog "  [env] AZURE_SUBSCRIPTION_ID    - not set" }

    if (-not [string]::IsNullOrEmpty($env:AZURE_RESOURCE_GROUP)) {
        if ([string]::IsNullOrEmpty($script:ResourceGroup)) {
            $script:ResourceGroup = $env:AZURE_RESOURCE_GROUP
            Write-InfoLog "  [env] AZURE_RESOURCE_GROUP     = $script:ResourceGroup"
        } else { Write-InfoLog "  [env] AZURE_RESOURCE_GROUP     = (skipped - config file value used)" }
    } else { Write-InfoLog "  [env] AZURE_RESOURCE_GROUP     - not set" }

    if (-not [string]::IsNullOrEmpty($env:AZURE_LOCATION)) {
        if ([string]::IsNullOrEmpty($script:Location)) {
            $script:Location = $env:AZURE_LOCATION
            Write-InfoLog "  [env] AZURE_LOCATION           = $script:Location"
        } else { Write-InfoLog "  [env] AZURE_LOCATION           = (skipped - config file value used)" }
    } else { Write-InfoLog "  [env] AZURE_LOCATION           - not set" }

    if (-not [string]::IsNullOrEmpty($env:AKSEDGE_CLUSTER_NAME)) {
        if ([string]::IsNullOrEmpty($script:ConfigClusterName)) {
            $script:ConfigClusterName = $env:AKSEDGE_CLUSTER_NAME
            if ([string]::IsNullOrEmpty($script:ClusterName)) { $script:ClusterName = $env:AKSEDGE_CLUSTER_NAME }
            Write-InfoLog "  [env] AKSEDGE_CLUSTER_NAME     = $script:ConfigClusterName"
        } else { Write-InfoLog "  [env] AKSEDGE_CLUSTER_NAME     = (skipped - config file value used)" }
    } else { Write-InfoLog "  [env] AKSEDGE_CLUSTER_NAME     - not set" }

    if (-not [string]::IsNullOrEmpty($env:AZURE_CONTAINER_REGISTRY)) {
        if ([string]::IsNullOrEmpty($script:ContainerRegistryName)) {
            $rawReg = $env:AZURE_CONTAINER_REGISTRY
            if ($rawReg -match '\.azurecr\.io$') {
                $script:ContainerRegistryLoginServer = $rawReg
                $script:ContainerRegistryName = $rawReg -replace '\.azurecr\.io$', ''
            } else {
                $script:ContainerRegistryName = $rawReg
                $script:ContainerRegistryLoginServer = "${rawReg}.azurecr.io"
            }
            Write-InfoLog "  [env] AZURE_CONTAINER_REGISTRY = $script:ContainerRegistryName"
        } else { Write-InfoLog "  [env] AZURE_CONTAINER_REGISTRY = (skipped - config file value used)" }
    } else { Write-InfoLog "  [env] AZURE_CONTAINER_REGISTRY - not set (will auto-generate)" }
}

function Test-ConfigConsistency {
    Write-Log "Validating configuration consistency..."

    # If cluster_info.json was not found (AKS-EE / single-machine workflow),
    # skip cross-validation and rely solely on aio_config.json / env vars.
    if ($null -eq $script:ClusterData) {
        if ($script:ConfigClusterName) {
            $script:ClusterName = $script:ConfigClusterName
            Write-InfoLog "cluster_info.json not loaded - cluster name from config/env: $script:ClusterName"
        } else {
            Write-WarnLog "No cluster_info.json and no cluster name in config or env vars."
            Write-WarnLog "You will be prompted for the cluster name during deployment."
        }
        return
    }

    # Check if both configs have cluster names defined
    $clusterInfoName = $script:ClusterData.cluster_name
    $aioConfigName = $script:ConfigClusterName
    
    if (-not $clusterInfoName) {
        Write-WarnLog "cluster_info.json was found but cluster_name is empty."
        Write-Host "  Falling back to env var / aio_config.json for cluster name." -ForegroundColor Yellow
        Write-Host "  (On the AKS-EE path this file is not required - safe to delete.)" -ForegroundColor Gray

        # 1. aio_config.json value already loaded into script:ConfigClusterName
        if ($script:ConfigClusterName) {
            $script:ClusterName = $script:ConfigClusterName
            Write-InfoLog "[CONFIG] Cluster name from aio_config.json: $script:ClusterName"
        # 2. Env var fallback
        } elseif ($env:AKSEDGE_CLUSTER_NAME) {
            $script:ClusterName = $env:AKSEDGE_CLUSTER_NAME
            Write-InfoLog "[ENV] Cluster name from AKSEDGE_CLUSTER_NAME: $script:ClusterName"
        # 3. Prompt as last resort
        } else {
            Write-Warning "[INPUT] AKSEDGE_CLUSTER_NAME not found in env vars or config."
            $script:ClusterName = Read-Host "Enter Cluster name"
            $env:AKSEDGE_CLUSTER_NAME = $script:ClusterName
            Write-Host "[INPUT] Saved as `$env:AKSEDGE_CLUSTER_NAME for this session." -ForegroundColor Cyan
        }
        $script:ClusterData = $null  # treat as not loaded
        return
    }
    
    if (-not $aioConfigName) {
        Write-InfoLog "aio_config.json does not specify cluster_name - using cluster_info.json value: $clusterInfoName"
        $script:ClusterName = $clusterInfoName
        return
    }
    
    # Both have cluster names - check if they match
    if ($clusterInfoName -ne $aioConfigName) {
        Write-Host ""
        Write-Host "============================================================================" -ForegroundColor Red
        Write-Host "WARNING: Cluster name mismatch detected!" -ForegroundColor Red
        Write-Host "============================================================================" -ForegroundColor Red
        Write-Host ""
        Write-Host "  cluster_info.json (from edge installer): $clusterInfoName" -ForegroundColor Yellow
        Write-Host "  aio_config.json (Azure config):          $aioConfigName" -ForegroundColor Yellow
        Write-Host ""
        Write-Host "This could indicate:" -ForegroundColor Cyan
        Write-Host "  - You're using the wrong config file for this cluster" -ForegroundColor Gray
        Write-Host "  - The aio_config.json was not updated after a fresh edge install" -ForegroundColor Gray
        Write-Host "  - These configs are for different environments" -ForegroundColor Gray
        Write-Host ""
        Write-Host "The cluster_info.json name comes from the actual edge device installer." -ForegroundColor Cyan
        Write-Host "The aio_config.json name is what will be used for Azure Arc registration." -ForegroundColor Cyan
        Write-Host ""
        
        Write-Host "Options:" -ForegroundColor Cyan
        Write-Host "  [1] Use cluster_info.json value: $clusterInfoName (recommended for fresh installs)" -ForegroundColor Gray
        Write-Host "  [2] Use aio_config.json value: $aioConfigName (if cluster is already Arc-enabled with this name)" -ForegroundColor Gray
        Write-Host "  [3] Abort and fix the configuration files" -ForegroundColor Gray
        Write-Host ""
        
        $choice = Read-Host "Enter choice (1/2/3)"
        
        switch ($choice) {
            "1" {
                $script:ClusterName = $clusterInfoName
                Write-InfoLog "Using cluster name from cluster_info.json: $clusterInfoName"
                Write-WarnLog "Consider updating aio_config.json to match: cluster_name = '$clusterInfoName'"
            }
            "2" {
                $script:ClusterName = $aioConfigName
                Write-InfoLog "Using cluster name from aio_config.json: $aioConfigName"
                Write-WarnLog "Consider updating cluster_info.json if this is a fresh install"
            }
            "3" {
                Write-Host ""
                Write-Host "To fix this, update one of the files to match:" -ForegroundColor Yellow
                Write-Host "  - Edit config/aio_config.json and set cluster_name to '$clusterInfoName'" -ForegroundColor Gray
                Write-Host "  - Or edit config/cluster_info.json and set cluster_name to '$aioConfigName'" -ForegroundColor Gray
                Write-Host ""
                Write-ErrorLog "Configuration mismatch - user chose to abort" -Fatal
            }
            default {
                Write-ErrorLog "Invalid choice. Aborting." -Fatal
            }
        }
    } else {
        Write-Success "Cluster names match: $clusterInfoName"
        $script:ClusterName = $clusterInfoName
    }
}

function Test-RequiredVariables {
    Write-Log "Validating required configuration..."

    $varHelp = @{
        "ResourceGroup" = @{ Env = "AZURE_RESOURCE_GROUP"; Label = "Azure resource group name" }
        "ClusterName"   = @{ Env = "AKSEDGE_CLUSTER_NAME"; Label = "Arc-enabled cluster name"  }
        "Location"      = @{ Env = "AZURE_LOCATION";       Label = "Azure region (e.g. eastus2)" }
    }

    $missingRequired = [System.Collections.Generic.List[string]]::new()
    if ([string]::IsNullOrEmpty($script:ResourceGroup)) { $missingRequired.Add("ResourceGroup") }
    if ([string]::IsNullOrEmpty($script:ClusterName))   { $missingRequired.Add("ClusterName") }
    if ([string]::IsNullOrEmpty($script:Location))      { $missingRequired.Add("Location") }

    if ($missingRequired.Count -eq 0) {
        Write-Success "All required variables present"
        Write-InfoLog "  Resource Group : $script:ResourceGroup"
        Write-InfoLog "  Cluster Name   : $script:ClusterName"
        Write-InfoLog "  Location       : $script:Location"
        return
    }

    Write-Host ""
    Write-Host ("=" * 60) -ForegroundColor Yellow
    Write-Host "REQUIRED CONFIGURATION MISSING" -ForegroundColor Yellow
    Write-Host ("=" * 60) -ForegroundColor Yellow
    Write-Host "The following values were not found in aio_config.json" -ForegroundColor Yellow
    Write-Host "or environment variables:" -ForegroundColor Yellow
    Write-Host ""
    foreach ($var in $missingRequired) {
        Write-Host "  - $($varHelp[$var].Label)" -ForegroundColor Red
        Write-Host "    set via: `$env:$($varHelp[$var].Env) = `"your-value`"" -ForegroundColor Gray
    }
    Write-Host ""
    Write-Host "Options to fix this without prompting:" -ForegroundColor Cyan
    Write-Host '  Option A  env vars:   $env:AZURE_RESOURCE_GROUP = "rg-my-iot"' -ForegroundColor White
    Write-Host '                         $env:AKSEDGE_CLUSTER_NAME = "my-cluster"' -ForegroundColor White
    Write-Host '                         $env:AZURE_LOCATION       = "eastus2"' -ForegroundColor White
    Write-Host "  Option B  config file: config/aio_config.json  (azure.resource_group, azure.cluster_name, azure.location)" -ForegroundColor White
    Write-Host "  Option C  CLI param:   -ConfigFile path/to/aio_config.json" -ForegroundColor White
    Write-Host ""

    foreach ($var in $missingRequired) {
        $value = Read-Host "  Enter $($varHelp[$var].Label)"
        if ([string]::IsNullOrWhiteSpace($value)) {
            Write-Host ""
            Write-ErrorLog "No value provided for $var. Cannot continue." -Fatal
        }
        switch ($var) {
            "ResourceGroup" { $script:ResourceGroup = $value.Trim() }
            "ClusterName"   { $script:ClusterName   = $value.Trim() }
            "Location"      { $script:Location      = $value.Trim() }
        }
    }

    # Final guard
    $stillMissing = @()
    if ([string]::IsNullOrEmpty($script:ResourceGroup)) { $stillMissing += "ResourceGroup  (env: AZURE_RESOURCE_GROUP)" }
    if ([string]::IsNullOrEmpty($script:ClusterName))   { $stillMissing += "ClusterName    (env: AKSEDGE_CLUSTER_NAME)" }
    if ([string]::IsNullOrEmpty($script:Location))      { $stillMissing += "Location       (env: AZURE_LOCATION)" }
    if ($stillMissing.Count -gt 0) {
        Write-Host ""
        foreach ($v in $stillMissing) { Write-ErrorLog "  Missing: $v" }
        Write-ErrorLog "Cannot continue - required configuration still missing." -Fatal
    }
}

